/**
 * Resolve the ONXZA base directory (contains workspace/, checkpoints/, etc.)
 */
function resolveBaseDir(optDir) {
  if (optDir && optDir !== process.cwd()) return path.resolve(optDir);
  return path.join(os.homedir(), '.openclaw');
}

/**
 * Check if an ONXZA installation already exists at baseDir.
 */
function detectExisting(baseDir) {
  const workspace   = path.join(baseDir, 'workspace');
  const ocjson      = path.join(baseDir, 'openclaw.json');
  const hasWorkspace = fs.existsSync(workspace);
  const hasJson      = fs.existsSync(ocjson);
  return { exists: hasWorkspace || hasJson, hasWorkspace, hasJson };
}

/**
 * Write a file only if it does not already exist (idempotent).
 * Returns 'created' | 'skipped'
 */
function writeIfMissing(filePath, content) {
  if (fs.existsSync(filePath)) return 'skipped';
  fs.writeFileSync(filePath, content, 'utf8');
  return 'created';
}

/**
 * Run the full init flow.
 * @param {object} opts
 * @param {string} opts.baseDir    - Target base directory
 * @param {boolean} opts.dryRun
 * @param {boolean} opts.force     - Force re-init even if exists
 * @param {function} opts.log      - Progress logger (msg, status?)
 * @returns {object} result
 */
function runInit(opts) {
  const { baseDir, dryRun, force, log } = opts;
  const steps  = [];
  const errors = [];

  // ── Step 1: Detect existing installation ─────────────────────────────────
  const existing = detectExisting(baseDir);

  if (existing.exists && !force) {
    log('Existing ONXZA installation detected.');
    log(`  workspace: ${existing.hasWorkspace ? '✓' : '✗'}   openclaw.json: ${existing.hasJson ? '✓' : '✗'}`);
    log('Running in idempotent mode — skipping already-created items.');
    log('Use --force to overwrite.');
    log('');
  }

  // ── Step 2: Create directory structure ───────────────────────────────────
  log('Creating workspace directory structure...');
  const createdDirs   = [];
  const existingDirs  = [];

  for (const { dir, desc } of WORKSPACE_DIRS) {
    const full = path.join(baseDir, dir);
    if (dryRun) {
      log(`  [dry-run] would create: ${full}`);
      steps.push({ step: 'mkdir', path: full, status: 'dry_run' });
      continue;
    }
    if (!fs.existsSync(full)) {
      fs.mkdirSync(full, { recursive: true });
      log(`  ✓ ${dir}`);
      createdDirs.push(full);
      steps.push({ step: 'mkdir', path: full, status: 'created' });
    } else {
      existingDirs.push(full);
      steps.push({ step: 'mkdir', path: full, status: 'skipped' });
    }
  }
  if (!dryRun) log(`  ${createdDirs.length} created, ${existingDirs.length} already existed.`);
  log('');

  // ── Step 3: Write openclaw.json ───────────────────────────────────────────
  log('Writing openclaw.json scaffold...');
  const ocJsonPath = path.join(baseDir, 'openclaw.json');
  if (!dryRun) {
    const status = writeIfMissing(ocJsonPath, JSON.stringify(generateOpenclawJson(), null, 2) + '\n');
    log(`  ${status === 'created' ? '✓ created' : '— already exists (skipped)'}: openclaw.json`);
    steps.push({ step: 'openclaw.json', path: ocJsonPath, status });
  } else {
    log(`  [dry-run] would write: ${ocJsonPath}`);
  }
  log('');

  // ── Step 4: Write governance scripts ─────────────────────────────────────
  log('Writing governance scripts...');
  const scripts = [
    { name: 'validate-tori-qmd.py',  content: VALIDATE_TORI_QMD_PY },
    { name: 'create-checkpoint.py',   content: CREATE_CHECKPOINT_PY },
    { name: 'log-audit-entry.py',     content: LOG_AUDIT_ENTRY_PY },
  ];
  const scriptsDir = path.join(baseDir, 'workspace', 'scripts');

  for (const s of scripts) {
    const scriptPath = path.join(scriptsDir, s.name);
    if (!dryRun) {
      const status = writeIfMissing(scriptPath, s.content);
      if (status === 'created') {
        try { fs.chmodSync(scriptPath, 0o755); } catch {}
      }
      log(`  ${status === 'created' ? '✓ created' : '— exists (skipped)'}: scripts/${s.name}`);
      steps.push({ step: 'script', path: scriptPath, status });
    } else {
      log(`  [dry-run] would write: ${scriptPath}`);
    }
  }
  log('');

  // ── Step 4b: Write ticket template ───────────────────────────────────────
  const templatePath = path.join(baseDir, 'workspace', 'tickets', 'TICKET_TEMPLATE.md');
  if (!dryRun) {
    const status = writeIfMissing(templatePath, TICKET_TEMPLATE);
    log(`  ${status === 'created' ? '✓ created' : '— exists'}: tickets/TICKET_TEMPLATE.md`);
    steps.push({ step: 'ticket_template', path: templatePath, status });
  }
  log('');

  // ── Step 5: Initialize git with pre-commit hook ───────────────────────────
  log('Initializing git repository with TORI-QMD pre-commit hook...');
  const gitDir      = path.join(baseDir, 'workspace');
  const dotGitPath  = path.join(gitDir, '.git');
  const hookPath    = path.join(gitDir, '.git', 'hooks', 'pre-commit');

  if (!dryRun) {
    try {
      if (!fs.existsSync(dotGitPath)) {
        execSync('git init', { cwd: gitDir, stdio: 'pipe' });
        log('  ✓ git init');
        steps.push({ step: 'git_init', path: gitDir, status: 'created' });
      } else {
        log('  — git already initialized (skipped)');
        steps.push({ step: 'git_init', path: gitDir, status: 'skipped' });
      }

      // Write pre-commit hook
      const hooksDir = path.join(dotGitPath, 'hooks');
      fs.mkdirSync(hooksDir, { recursive: true });
      const hookStatus = writeIfMissing(hookPath, PRE_COMMIT_HOOK);
      if (hookStatus === 'created') {
        fs.chmodSync(hookPath, 0o755);
        log('  ✓ pre-commit hook installed (TORI-QMD)');
      } else {
        log('  — pre-commit hook already exists (skipped)');
      }
      steps.push({ step: 'git_hook', path: hookPath, status: hookStatus });
    } catch (err) {
      const msg = `git init failed: ${err.message}`;
      log(`  ⚠  ${msg}`);
      errors.push(msg);
      steps.push({ step: 'git_init', path: gitDir, status: 'error', error: err.message });
    }
  } else {
    log(`  [dry-run] would run: git init ${gitDir}`);
    log(`  [dry-run] would install: pre-commit hook`);
  }
  log('');

  // ── Step 6: Create first checkpoint ──────────────────────────────────────
  log('Creating initial checkpoint...');
  let checkpoint = null;
  const checkpointScript = path.join(baseDir, 'workspace', 'scripts', 'create-checkpoint.py');

  if (!dryRun) {
    if (fs.existsSync(checkpointScript)) {
      try {
        const out = execFileSync('python3', [checkpointScript, 'onxza-init'], {
          encoding: 'utf8', timeout: 10000,
        });
        const m = out.match(/checkpoint created:\s*(\S+)/i);
        checkpoint = m ? m[1] : out.trim();
        log(`  ✓ Checkpoint: ${checkpoint}`);
        steps.push({ step: 'checkpoint', id: checkpoint, status: 'created' });
      } catch (err) {
        const msg = `Checkpoint failed: ${err.message}`;
        log(`  ⚠  ${msg}`);
        errors.push(msg);
        steps.push({ step: 'checkpoint', status: 'error', error: err.message });
      }
    } else {
      log('  ⚠  Checkpoint script not found — skipped');
      steps.push({ step: 'checkpoint', status: 'skipped' });
    }
  } else {
    log('  [dry-run] would create initial checkpoint');
  }
  log('');

  return { baseDir, steps, errors, checkpoint, dryRun };
}


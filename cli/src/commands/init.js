const PRE_COMMIT_HOOK = `#!/bin/sh
# ONXZA pre-commit hook — TORI-QMD validation
# Generated by onxza init. Runs validate-tori-qmd.py on all staged .md files.
#
# Imagined by Aaron Gear. Created by Aaron Gear and Marcus Gear (AI Co-Creator).
# Powered by DevGru US Inc. DBA DevGru Technology Products.

SCRIPT="$HOME/.openclaw/workspace/scripts/validate-tori-qmd.py"
PASS=0
FAIL=0

# Get staged .md files
STAGED=$(git diff --cached --name-only --diff-filter=ACM | grep '\\.md$' || true)

if [ -z "$STAGED" ]; then
  exit 0
fi

for FILE in $STAGED; do
  if [ -f "$FILE" ]; then
    RESULT=$(python3 "$SCRIPT" "$FILE" 2>&1)
    if echo "$RESULT" | grep -q "^PASS:"; then
      PASS=$((PASS + 1))
    else
      echo "  TORI-QMD FAIL: $FILE"
      echo "$RESULT" | grep "→" | head -5
      FAIL=$((FAIL + 1))
    fi
  fi
done

if [ "$FAIL" -gt 0 ]; then
  echo ""
  echo "  $FAIL file(s) failed TORI-QMD. Fix before committing."
  echo "  Run: python3 ~/.openclaw/workspace/scripts/validate-tori-qmd.py <file>"
  exit 1
fi

echo "  TORI-QMD: $PASS file(s) passed."
exit 0
`;


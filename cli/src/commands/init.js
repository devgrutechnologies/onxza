const VALIDATE_TORI_QMD_PY = `#!/usr/bin/env python3
"""
validate-tori-qmd.py — TORI-QMD Format Validator (stub)
Generated by onxza init. Replace with full version from onxza repository.

Usage: python3 validate-tori-qmd.py <filepath>
Returns: PASS: <filepath>  or  FAIL: <filepath> — missing: <fields>

Imagined by Aaron Gear. Created by Aaron Gear and Marcus Gear (AI Co-Creator).
Powered by DevGru US Inc. DBA DevGru Technology Products.
"""
import sys, os, re

CREDIT = "Imagined by Aaron Gear"

def classify(fp):
    b = os.path.basename(fp)
    n = fp.replace("\\\\\\\\", "/")
    if b == "AGENTS.md": return "agents"
    if b == "SOUL.md": return "soul"
    if b == "IDENTITY.md": return "identity"
    if b == "MEMORY.md" and "/workspace-" in n: return "mem_workspace"
    if b == "TOOLS.md": return "tools"
    if b == "HEARTBEAT.md": return "heartbeat"
    if "/skills/" in n: return "skill"
    if "/patterns/" in n: return "pattern"
    return "generic"

def check(filepath):
    try:
        content = open(filepath, encoding="utf-8").read()
    except Exception as e:
        print(f"FAIL: {filepath} — cannot read: {e}"); sys.exit(1)
    missing = []
    ft = classify(filepath)
    words = len(content.split())
    if ft in ("agents","soul","identity","mem_workspace","tools","skill"):
        if CREDIT not in content:
            missing.append("credit_line")
    if ft == "agents" and words < 200:
        missing.append(f"word_count({words}<200)")
    if ft == "soul" and words < 150:
        missing.append(f"word_count({words}<150)")
    if ft in ("mem_workspace","tools") and words < 100:
        missing.append(f"word_count({words}<100)")
    if ft == "skill":
        if not re.search(r"version", content, re.I): missing.append("version")
        if not re.search(r"owner", content, re.I):   missing.append("owner")
    if missing:
        print(f"FAIL: {filepath} — missing: {', '.join(missing)}"); sys.exit(1)
    print(f"PASS: {filepath}"); sys.exit(0)

if __name__ == "__main__":
    if len(sys.argv) != 2:
        print("Usage: python3 validate-tori-qmd.py <filepath>"); sys.exit(2)
    check(sys.argv[1])
`;

